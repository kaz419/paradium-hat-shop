// Apply translations
// ============================================================
function applyLang(lang) {
  const t = translations[lang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  // Set lang attribute for font rendering
  document.documentElement.lang = lang;
  document.body.className = lang !== 'en' ? `lang-${lang}` : '';

  // Update active button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Persist choice
  localStorage.setItem('hatHouseLang', lang);
}

// ============================================================
// Scroll-triggered fade-up animation (IntersectionObserver)
// ============================================================
function initScrollAnimations() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  document.querySelectorAll('.anim-fade-up').forEach(el => observer.observe(el));
}

// ============================================================
// Number counter animation
// ============================================================
function animateCounter(el) {
  const target = parseInt(el.dataset.count, 10);
  if (!target) return;
  const duration = 1200;
  const start = performance.now();

  function step(now) {
    const elapsed = now - start;
    const progress = Math.min(elapsed / duration, 1);
    // ease-out cubic
    const eased = 1 - Math.pow(1 - progress, 3);
    el.textContent = Math.floor(eased * target).toLocaleString();
    if (progress < 1) requestAnimationFrame(step);
    else el.textContent = target.toLocaleString() + '+';
  }
  requestAnimationFrame(step);
}

function initCounters() {
  const counterObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        animateCounter(entry.target);
        counterObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.5 });

  document.querySelectorAll('.stat-num[data-count]').forEach(el => counterObserver.observe(el));
}

// ============================================================
// Floating WA button – collapse/expand on scroll
// ============================================================
function initFloatWA() {
  const btn = document.querySelector('.float-wa');
  if (!btn) return;
  let lastY = 0;
  window.addEventListener('scroll', () => {
    const y = window.scrollY;
    if (y > 300 && y > lastY) {
      // scrolling down → compact (phones already compact via CSS)
      btn.classList.add('compact');
    } else {
      btn.classList.remove('compact');
    }
    lastY = y;
  }, { passive: true });
}

